int	ft_atoi(const char *str)
{
	int num;
	int i;
	int neg;

	i = 0;
	num = 0;
	neg = 1;
	while (str[i] != '\0')
	{
		if (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
			i++;
		if (str[i] == '-' || str[i] == '+')
		{
			if(str[i] == '-')
				neg = -1;
			i++;
		}
		if (str[i] >= '0' && str[i] <= '9')
		{
			num = num * 10 + (str[i] - '0');
		}
		i++;
	}
	return (num * neg);
}
